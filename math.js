// Double a number
function double(n) {
  return n * 2;
}
console.log("double(2) →", double(2));
console.log("double(5) →", double(5));
console.log("double(-3) →", double(-3));
console.log("double(0) →", double(0));


// Multiply two numbers
function multiply(a, b) {
  return a * b;
}
console.log("multiply(2, 3) →", multiply(2, 3));
console.log("multiply(0, 100) →", multiply(0, 100));
console.log("multiply(-4, 5) →", multiply(-4, 5));
console.log("multiply(7, -8) →", multiply(7, -8));


// Square a number
function square(n) {
  return n * n;
}
console.log("square(2) →", square(2));
console.log("square(0) →", square(0));
console.log("square(-4) →", square(-4));
console.log("square(10) →", square(10));

// Check if a number is odd
function isOdd(n) {
  return n % 2 !== 0;
}
console.log("isOdd(3) →", isOdd(3));
console.log("isOdd(4) →", isOdd(4));
console.log("isOdd(0) →", isOdd(0));
console.log("isOdd(-7) →", isOdd(-7));

// Get the remainder of a divided by b
function modulo(a, b) {
  return a % b;
}
console.log("modulo(10, 3) →", modulo(10, 3));
console.log("modulo(15, 5) →", modulo(15, 5));
console.log("modulo(7, 4) →", modulo(7, 4));
console.log("modulo(9, 2) →", modulo(9, 2));

